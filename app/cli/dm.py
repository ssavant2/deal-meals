    typer.echo(f"  entry: {entry_id}")
    if not run_gates:
        typer.echo("Skipped gates (--no-run-gates).")
        return
    gate_status = _run_keyword_synonym_light_gates(paths=paths, report_root=report_root)
    raise typer.Exit(gate_status)


